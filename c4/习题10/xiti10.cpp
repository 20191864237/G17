#include <stdio.h>
int main()
{
	int i,a;
	printf("输入利润");
	scanf("%d",&i);
		if(i<=100000)
		{
			a=i*0.1;
		}
		else if(i>100000&&i<=200000)
		{
			a=100000*0.1+(i-100000)*0.075;
		}
		else if(i>200000&&i<=400000)
		{
			a=200000*0.1+(i-200000)*0.05;
		}
		else if(i>400000&&i<=600000)
		{
			a=400000*0.1+(i-400000)*0.03;
		}
		else if(i>600000&&i<=1000000)
		{
			a=600000*0.1+(i-600000)*0.015;
		}
		else 
		{
			a=1000000*0.1+(i-1000000)*0.01;
		}
		printf("奖金为%d\n",a);
		return 0;
}
